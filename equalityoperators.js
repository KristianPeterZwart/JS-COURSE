
// Strict Equality Operator (Type + Value) USE THIS FOR ACCURACY
console.log(1 === 1) // compares number to number equals true
console.log('1' === 1) // compares string to a number equals false

// Loose Equality Operator - looks at left side
console.log(1 == 1)
console.log('1' == 1)
console.log(true == 1)