
let x = 1

// Relational
console.log(x > 0) // greater than // shows Boolean operator true
console.log(x >= 1) // greater than or equal to // shows Boolean operator true
console.log(x < 1) // lesser than // shows Boolean operator false
console.log(x <= 1) // greater than or equal // shows Boolean operator true

// Equality
console.log(x === 1) // equal to // shows Boolean operator true
console.log(x !== 1) // not equal to // shows Boolean operator false